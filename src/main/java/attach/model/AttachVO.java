package attach.model;

public class AttachVO {
	private int attach_seq;
	private int board_seq;
	private String attach_route;
	
	
	public int getAttach_seq() {
		return attach_seq;
	}
	public void setAttach_seq(int attach_seq) {
		this.attach_seq = attach_seq;
	}
	public int getBoard_seq() {
		return board_seq;
	}
	public void setBoard_seq(int board_seq) {
		this.board_seq = board_seq;
	}
	public String getAttach_route() {
		return attach_route;
	}
	public void setAttach_route(String attach_route) {
		this.attach_route = attach_route;
	}
	
	
}
